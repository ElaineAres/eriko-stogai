const initReveal = () => {
  if (!("IntersectionObserver" in window)) {
    revealItems.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const revealObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -5% 0px"
    }
  );

  revealItems.forEach((item) => revealObserver.observe(item));
};

const initActiveSections = () => {
  const sections = [...navLinks]
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean);

  if (!sections.length || !("IntersectionObserver" in window)) {
    return;
  }

  const sectionObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        const targetId = `#${entry.target.id}`;

        navLinks.forEach((link) => {
          const isActive = link.getAttribute("href") === targetId;
          link.classList.toggle("is-active", isActive);
        });
      });
    },
    {
      threshold: 0.45,
      rootMargin: "-15% 0px -45% 0px"
    }
  );

  sections.forEach((section) => sectionObserver.observe(section));
};

const initHeroMotion = () => {
  if (!heroSection || prefersReducedMotion.matches) {
    return;
  }

  let pointerX = 0;
  let pointerY = 0;
  let scrollOffset = 0;
  let frame = null;

  const renderMotion = () => {
    heroSection.style.setProperty("--hero-shift-x", `${pointerX}px`);
    heroSection.style.setProperty("--hero-shift-y", `${pointerY + scrollOffset}px`);
    heroSection.style.setProperty("--hero-shift-soft-x", `${pointerX * 0.45}px`);
    heroSection.style.setProperty("--hero-shift-soft-y", `${(pointerY + scrollOffset) * 0.4}px`);
    frame = null;
  };

  const requestMotionFrame = () => {
    if (frame !== null) {
      return;
    }

    frame = window.requestAnimationFrame(renderMotion);
  };

  const updateScrollMotion = () => {
    const rect = heroSection.getBoundingClientRect();
    const viewportHeight = window.innerHeight || 1;
    const progress = ((viewportHeight - rect.top) / (viewportHeight + rect.height)) - 0.5;
    const clamped = Math.max(-1, Math.min(1, progress));
    scrollOffset = clamped * -18;
    requestMotionFrame();
  };

  heroSection.addEventListener("pointermove", (event) => {
    if (window.innerWidth <= 900) {
      return;
    }

    const rect = heroSection.getBoundingClientRect();
    const x = ((event.clientX - rect.left) / rect.width) - 0.5;
    const y = ((event.clientY - rect.top) / rect.height) - 0.5;

    pointerX = x * 26;
    pointerY = y * 20;
    requestMotionFrame();
  });

  heroSection.addEventListener("pointerleave", () => {
    pointerX = 0;
    pointerY = 0;
    requestMotionFrame();
  });

  window.addEventListener("scroll", updateScrollMotion, { passive: true });
  window.addEventListener("resize", updateScrollMotion);
  updateScrollMotion();
};

