const getNestedValue = (source, path) => path.split(".").reduce((accumulator, key) => accumulator?.[key], source);

const applyLanguage = (lang) => {
  const languagePack = translations[lang] || translations.lt;

  translatableItems.forEach((item) => {
    const key = item.dataset.i18n;
    const value = getNestedValue(languagePack, key);

    if (typeof value === "undefined") {
      return;
    }

    const attribute = item.dataset.i18nAttr;

    if (attribute) {
      item.setAttribute(attribute, value);
      return;
    }

    if (item.dataset.i18nHtml !== undefined) {
      item.innerHTML = value;
      return;
    }

    item.textContent = value;
  });

  document.documentElement.lang = lang;
  document.title = languagePack.meta.title;
  metaDescription?.setAttribute("content", languagePack.meta.description);
  ogTitle?.setAttribute("content", languagePack.meta.ogTitle);
  ogDescription?.setAttribute("content", languagePack.meta.ogDescription);
  ogLocale?.setAttribute("content", languagePack.meta.locale);
  ogImageAlt?.setAttribute("content", languagePack.meta.ogImageAlt);

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.setLang === lang);
  });

  if (menuToggle) {
    menuToggle.setAttribute("aria-label", languagePack.header.menuLabel);
  }

  if (floatingCall) {
    floatingCall.setAttribute("aria-label", languagePack.floatingCall);
  }

  localStorage.setItem("preferredLanguage", lang);
};

const syncSeoUrls = () => {
  if (!window.location.protocol.startsWith("http")) {
    return;
  }

  const cleanUrl = window.location.href.split("#")[0];
  const absoluteImage = new URL("images/8a5ce9258b19db7de4cc5b123d66bbfd.jpg", cleanUrl).href;

  canonical?.setAttribute("href", cleanUrl);
  ogUrl?.setAttribute("content", cleanUrl);
  ogImage?.setAttribute("content", absoluteImage);

  if (!schemaScript) {
    return;
  }

  try {
    const schema = JSON.parse(schemaScript.textContent);
    schema.url = cleanUrl;
    schema.image = absoluteImage;
    schemaScript.textContent = JSON.stringify(schema);
  } catch (error) {
    console.warn("Schema sync skipped:", error);
  }
};

